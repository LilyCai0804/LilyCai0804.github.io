	function populatePage(sortedArray){

		//gets rid of whatever was there before
		$('#drawings').html('')

		for(var i=0; i<sortedArray.length; i++){

			var drawingsDiv = $('<div/>', {
				'class': 'drawings'
			})
				.html(sortedArray[i].pics)

			$('#drawings').append(drawingsDiv);
		}

	}



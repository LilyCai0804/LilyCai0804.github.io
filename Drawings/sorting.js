	function sortBySize() {
	drawings.sort(function(a, b){
			if (a.size>b.size){
				return 1;
			}
			if (a.size<b.size){
				return -1;
			}
			return 0;
		});
	};


	function sortByMood() {
	drawings.sort(function(a, b){
			if (a.mood>b.mood){
				return 1;
			}
			if (a.mood<b.mood){
				return -1;
			}
			return 0;
		});
	};



